public enum Tickets
{
  
  TICKET_01("Lichtenberg", 2330),
  TICKET_02("Friedrichshain", 4130),
  TICKET_03("Kreuzberg", 1280),
  TICKET_04("Prenzlauer Berg", 3200),
  TICKET_05("Neuköln", 560),
  TICKET_06("Tempelhof", 830),
  TICKET_07("Friedrichsfelde", 900),
  TICKET_08("Karlshorst", 870),
  TICKET_09("Schöneweide", 5210),
  TICKET_10("Marzahn", 2040),
  TICKET_11("Adlershof", 1160),
  TICKET_12("Kaulsdorf", 4160);

  public String fahrt;
  
  public int tarif;

  //##########################################################################
  /**
  */
  //##########################################################################
	private Tickets(String dieFahrt, int derTarif)
	{
    fahrt = dieFahrt;
    tarif = derTarif;
  }

  //##########################################################################
  /**
  */
  //##########################################################################
  public static double centZuEuro(int wert)
  {
    return ((double)wert / 100);
  }
  
  //##########################################################################
  /**
  */
  //##########################################################################
  public static int euroZuCent(double wert)
  {
    return (int)(wert * 100);
  }
  
  //##########################################################################
  /**
  */
  //##########################################################################
  public double getTarifEuro() 
  {
    return centZuEuro(tarif);
  }
  
  //##########################################################################
  /**
  */
  //##########################################################################
  public int getTarifCent() 
  {
    return tarif;
  }
  
  //##########################################################################
  /**
  */
  //##########################################################################
  public String getFahrt() 
  {
    return fahrt;
  }

}
